// Label --
//    The **label** of a modular curve $X_H$ is the same as the label of the open subgroup $H\le \GL_2(\widehat\Z)$. When $\det(H)=\widehat\Z^\times$ and $-I\in H$ this label has the form $\texttt{N.i.g.c.n}$, where

//    - $N$ is the level of $H$
//    - $i$ is the index of $H$
//    - $g$ is the genus of $H$
//    - $c$ is a base-26 ordinal that uniquely identifies the Gassmann class of $H$ among groups of the same level, index, and genus.
//    - $n$ is a positive integer that distinguishes nonconjugate subgroups of the same level, index, genus, and Gassmann class.

//    When $\det(H)=\widehat\Z^\times$ and $-I\not \in H$ this label has the form $\texttt{N.i.g-M.c.m.n}$, where $N$, $i$, and $g$ are as above, and

//    - $N$ is the level of $H$
//    - $i$ is the index of $H$
//    - $g$ is the genus of $H$
//    - $M$ is the level of $H':=\langle H, -I\rangle$
//    - $c$ is the base-26 ordinal that identifies the Gassmann class of $H'$
//    - $m$ is the positive integer in the label $\texttt{M.j.g.c.m}$ of $H'$, where $i=2j$.
//    - $n$ is a positive integer that distinguishes $H$ from nonconjugate refinements of $H'$ of the same level.


//CP label (CPlabel) --
//    Modular curves are identified in the literature using several different labeling conventions.  In addition to the standard names for the classical modular curves $X(N), X_0(N), X_1(N), X_{\mathrm ns}^+(N), X_{\mathrm sp}^+(N)$, etc., there are a number of labeling systems that are used in different contexts, including:

//    - **(CP)** Cummins and Pauli labels $\texttt{NA}^\texttt{g}$,are used to identify congruence subgroups of $\PSL_2(\Z)$ of level $N$ and genus $g\le 24$ on <a href="https://mathstats.uncg.edu/sites/pauli/congruence/">this site</a>, which is based on \cite{mr:2016709}.<br>  Note that Cummins and Pauli work up to $\PGL_2(\Z)$-conjugacy, thus for some open $H$ in $\GL_2(\widehat\Z)$ the group $\pm H\cap \SL_2(\widehat \Z)$ may have more than one CP label.
//    - **(RZB)** Rouse and Zureick-Brown labels $X\texttt{m}$ with $1\le m\le 727$ are used for modular curves $X_H$ of $2$-power level on <a href="https://users.wfu.edu/rouseja/2adic/">this site</a>, based on \cite{doi:10.1007/s40993-015-0013-7}.<br>  Note that Rouse and Zureick-Brown put the action on the right, rather than the left (so one should transpose $H$ when comparing with their tables).
//    - **(RSZB)** Rouse, Sutherland, and Zureick-Brown labels $\texttt{N.i.g.n}$ identify open subgroups of $\GL_2(\widehat\Z)$ according to their level $N$, index $i$, and genus $g$, as defined in \cite{doi:10.1017/fms.2022.38}.
//    - **(S)** Sutherland labels $\texttt{NS.a.b.c}$ for groups of prime level $N$ were introduced in \cite{arXiv:1504.07618,MR:3482279} and are used to identify mod-$\ell$ Galois images.
//    - **(SZ)** Sutherland and Zywina labels $\texttt{MA}^\texttt{g}\texttt{-Na}$ are used in \cite{arXiv:1605.03988,mr:3671434} to identify modular curves of prime power level with infinitely many rational points.


//$\operatorname{GL}_2(\mathbb{Z}/N\mathbb{Z})$-generators (generators) --
//    Let $H$ be an open subgroup of $\GL_2(\widehat\Z)$ of level $N$, let $\pi_N\colon \GL_2(\widehat\Z)\to \GL_2(\Z/N\Z)$ be the natural projection, and let $E$ be an elliptic curve over a number field $K$.

//    An **$H$-level structure** on $E$ is the $H$-orbit $[\iota]_H:=\{ h\circ \iota\colon h\in \pi_N(H)\}$ of an isomorphism $\iota\colon E[N]\overset{\sim}{\rightarrow}(\Z/N\Z)^2$.

//    An $H$-level structure on $E$ is **rational** if it lies in a $\Gal_K$-stable isomorphism class of pairs $(E,[\iota]_H)$, where $\sigma\in \Gal_K$ acts via $(E,[\iota]_H)\mapsto (E^\sigma,[\iota\circ\sigma^{-1}]_H)$.
//    Two pairs $(E,[\iota]_H)$ and $(E',[\iota']_H)$ are isomorphic if there is an isomorphism $\phi\colon E\to E'$ that induces an isomorphism $\phi_N\colon E[N]\to E'[N]$ for which $\phi_N^*([\iota']_H) = [\iota]_H$.

//    If $E$ admits a rational $H$-level structure $[\iota]_H$ then image of its adelic Galois representation $\rho_E\colon \Gal_K\to \GL_2(\widehat\Z)$ is conjugate to a subgroup of $H$ and the isomorphism class of $(E,[\iota]_H)$ is a non-cuspidal $K$-rational point on the modular curve $X_H$.

//    When $-1\in H$ every non-cuspidal $K$-rational point on $X_H$ arises in this way.  When $-1\not\in H$ this is almost true, but there may be exceptions at points with $j(E)=0,1728$.

//    Invariants of a rational $H$-level structure include:

//    - **Cyclic $\boldsymbol{N}$-isogeny field degree**: the minimal degree of an extension $L/K$ over which the base change $E_L$ admits a rational cyclic isogeny of degree $N$; equivalently, the index of the largest subgroup of $H$ fixing a subgroup of $(\Z/N\Z)^2$ isomorphic to $\Z/N\Z$.
//    - **Cyclic $\boldsymbol{N}$-torsion field degree**: the minimal degree of an extension $L/K$ for which $E_L$ has a rational point of order $N$; equivalently, the index of the largest subgroup of $H$ that fixes a point of order $N$ in $(\Z/N\Z)^2$.
//    - **N-torsion field degree** the minimal degree of an extension $L/K$ for which $E[N]\subseteq E(L)$; this is simply the cardinality of the reduction of $H$ to $\GL_2(\Z/N\Z)$.


